package com.cuubez.visualizer.context;


public class HttpCodeMetaData {

    public String name;
    public String detail;

    public HttpCodeMetaData(String name, String detail) {
        this.name = name;
        this.detail = detail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
